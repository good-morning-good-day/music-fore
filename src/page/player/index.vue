<script>





export default{

    data(){
        return{
            
            //歌词
            word:"",
            //歌词时间戳数组
            miltime:"",
            //歌词行
            word_line:"",
            //播放器 
            audio:new Audio(),
            //歌曲网址
            music_url:"",
            //音量大小
            music_volume:0.3,
            //单首音乐最后播放时间
            music_play_time:0,
            //定时器
            interval:0,
            //滚动条离顶点距离
            targetPosition :0,
            //歌曲总时长
            timeNum:0,
    }
        
       
    },
    methods:{
        formatTooltip(val){
            return val/this.miltime[this.miltime.length-1] ;
        },
        wordAnalysis(word,that){
            console.log(word)
            const words = word.split("\n")
             let miltime1 = []
             let word_line1 = []
            for(let i=0;i<words.length-1;i++){
                let minute = parseInt(/\[.*\d:/.exec(words[i])[0].replace("[","").replace(":",""))
                let second =  Number(/\:\d\d\./.exec(words[i])[0].replace(":","").replace(".",""))
                let millisecond = Number(/\..*\]/.exec(words[i])[0].replace("]","").replace(".",""))
                let milliToSecond = millisecond>=500?1:0
                let miltime = milliToSecond + second + minute*60
                // console.log(second + minute*60)               
                miltime1.push(miltime)
                let word_line = /\].*/.exec(words[i])[0].replace("]","")
                word_line1.push(word_line)
            }
            console.log(miltime1)
            that.miltime = miltime1
            that.word_line = word_line1
        },
        audio_play:function(){

            var player_down_left_icon2 = document.getElementById("player_down_left_icon2")
            player_down_left_icon2.style.display = "block";
            var player_down_left_icon3 = document.getElementById("player_down_left_icon3")
            player_down_left_icon3.style.display = "none";
            console.log(this.audio)
            this.audio.src = this.music_url
            this.audio.volume = this.music_volume
            this.audio.currentTime = this.music_play_time 
            this.audio.play()
            var that =this
            this.interval = setInterval(function(){ that.music_play_time=that.music_play_time+1;},1000)

        },
        audio_pause:function(){
            var player_down_left_icon2 = document.getElementById("player_down_left_icon2")
            player_down_left_icon2.style.display = "none";
            var player_down_left_icon3 = document.getElementById("player_down_left_icon3")
            player_down_left_icon3.style.display = "block";
            this.audio.pause()
            clearInterval(this.interval);
        },
        setVolume:function(){
            let player_down_right_volum = document.getElementById("player_down_right_volum")
            player_down_right_volum.style.display="flex"
            player_down_right_volum.style.zIndex="2"
        }

    },

    mounted:function(){
        // var that =this;
        this.$axios.get('http://127.0.0.1:8081/api/music/getSongById?id=1').then(resp=>{
            console.log(resp.data)
            this.word_url = resp.data.data.word_url
            this.music_url = resp.data.data.url
            this.timeNum = Number(/.*:/.exec(resp.data.data.time)[0].replace(":",""))*60+Number(/:.*/.exec(resp.data.data.time)[0].replace(":",""))
            //获取歌词并解析歌词
            this.$axios.get(this.word_url).then(resp=>{
                this.word = resp.data
                this.wordAnalysis(this.word,this)
            })
            //进入页面自动播放音乐
            this.audio_play()
        })
    },
    watch:{
        music_play_time : function(newValue,oldValue){
            // console.log(newValue)
            var wordBox = document.getElementById("player-up-right-word");
            for(let i =0 ;i<this.miltime.length;i++){
                // console.log(this.miltime[i])
                if(newValue==this.miltime[i]){
                   this.targetPosition =this.targetPosition+40
                    wordBox.scrollTo(0,this.targetPosition)
                }
            }
          

            if(newValue == this.timeNum){
                clearInterval(this.interval)
           }
        }
        
    }
}

</script>